<script>
    function enviarTotal() {
        const totalPagar = document.querySelector('carrito-precio-total').textContent.trim();
        localStorage.setItem('totalPagar', totalPagar);
    }
    function agregarAlCarrito(nombre, precio) {
        const carrito = JSON.parse(localStorage.getItem('carrito')) || {};
        
        if (carrito[nombre]) {
            carrito[nombre].cantidad += 1;
        } else {
            carrito[nombre] = {
                precio: precio,
                cantidad: 1
            };
        }
        
        localStorage.setItem('carrito', JSON.stringify(carrito));

        enviarTotal();
    }

    document.querySelectorAll('.boton-item').forEach(function(boton) {
        boton.addEventListener('click', function() {
            const item = this.parentNode;
            const titulo = item.querySelector('.titulo-item').textContent;
            const precio = parseFloat(item.querySelector('.precio-item').textContent.slice(1));
            agregarAlCarrito(titulo, precio);
        });
    });
</script>
